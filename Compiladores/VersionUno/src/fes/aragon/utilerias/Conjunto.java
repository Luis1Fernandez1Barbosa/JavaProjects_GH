package fes.aragon.utilerias;

public class Conjunto {
	public static boolean letraA(char c) {
		int valor = c;
		boolean valido = false;
		if (valor == 'a') {
			valido = true;
		}
		return valido;
	}

	public static boolean letraH(char c) {
		int valor = c;
		boolean valido = false;
		if (valor == 'h') {
			valido = true;
		}
		return valido;
	}
	
	public static boolean letraZ(char c) {
		int valor = c;
		boolean valido = false;
		if (valor == 'z') {
			valido = true;
		}
		return valido;
	}
}